class pattern_10 {
    public static void main(String[] args) {
       int rows = 4;
       for (int i = 1; i <= rows; i++) {
        if (i%2==1) {
            System.out.println("*");
        } else  {
            System.out.println("**");
        }
       }
      
    }
}

